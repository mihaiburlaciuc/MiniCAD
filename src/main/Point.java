package main;

public class Point {
    private Integer x;
    private Integer y;

    public Point(final Integer x, final Integer y) {
        this.x = x;
        this.y = y;
    }

    /**
     * getter x.
     * @return
     */
    public Integer getX() {
        return x;
    }

    /**
     * getter y.
     * @return
     */
    public Integer getY() {
        return y;
    }
}
